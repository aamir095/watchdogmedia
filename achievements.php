Documenting the project activities was essential for record and understanding the impact on the target people. Thus, a documentary was prepared including the achievements made by beneficiaries, progressive changes made over the years and the effective strategies/approaches for the institutional capacity for National Farmer's Group Federation were important. The documents could be used to share the impact of the project on target population. 
